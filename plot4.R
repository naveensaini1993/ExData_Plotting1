data <- read.table("household_power_consumption.txt",header = T,sep = ";")

p4<- subset(data,data$Date=="1/2/2007" | data$Date=="2/2/2007")
TimeDate <- strptime(paste(p4$Date, p4$Time, sep=" "), "%d/%m/%Y %H:%M:%S")
actpow <- as.numeric(p4$Global_active_power)
volt <- as.numeric(p4$Voltage)
sm1 <- as.numeric(p4$Sub_metering_1)
sm2 <- as.numeric(p4$Sub_metering_2)
sm3 <- as.numeric(p4$Sub_metering_3)
rctpow <- as.numeric(p4$Global_reactive_power)

png("plot4.png", width=480, height=480)

par(mfrow = c(2, 2))

plot(TimeDate, actpow, type="l", xlab="", ylab="Global Active Power")

plot(TimeDate, volt, type="l", xlab="datetime", ylab="Voltage")

plot(TimeDate, sm1, type="l", xlab="", ylab="Energy sub metering")
lines(TimeDate,sm2,type="l", col="red")
lines(TimeDate,sm3,type="l", col="blue")
legend("topright",legend = c("Sub_metering_1", "Sub_metering_2", "Sub_metering_3"), lty=1, lwd=2, col=c("black", "red", "blue"))

plot(TimeDate, rctpow, type="l", xlab="datetime", ylab="Global_reactive_power")

dev.off()